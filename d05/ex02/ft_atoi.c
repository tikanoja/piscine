int	ft_atoi(char *str)
{
	int	c;
	int	i;
	int	sign;

	c = 0;
	i = 0;
	sign = 1;
	while (str[c] == 32 || (str[c] >= 9 && str[c] <= 13))
	{
		c++;
	}
	if (str[c] == 45)
		sign = -1;
		c++;
	if (str[c] == 43)
		c++;
	while (str[c] != '\0')
	{
		if (str[c] < 48 || str[c] > 57)
			str[c] = '\0';
		i = (i * 10) + (str[c] - 48);
		c++;
	}
	return (i * sign);
}
