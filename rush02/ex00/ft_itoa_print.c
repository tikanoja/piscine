#include <unistd.h>

void	ft_itoa_print(int num)
{
	int		i;
	int		rem;
	char	str[16];

	i = 0;
	while (num != 0)
	{
		rem = num % 10;
		rem = rem + '0';
		str[i] = rem;
		num /= 10;
		i ++;
	}
	while (i > 0)
	{
		write (1, &str[i - 1], 1);
		i --;
	}
}
